public class varialbes {
    public static void main(String[] args) {
        int par = 50;
        int base = 50;

        if (par == base){
            for (int i = 1; i <= base; i++){
                System.out.print(i + "-");
            }
        }else{
            System.out.println("No retornar el numero");}
    }
}
